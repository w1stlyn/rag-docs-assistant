def format_context(chunks: list[dict]) -> str:
    parts: list[str] = []
    for i, c in enumerate(chunks, start=1):
        meta = c.get("metadata") or {}
        src = meta.get("source", "?")
        page = meta.get("page", "?")
        parts.append(f"[Фрагмент {i} | {src} | стр. {page}]\n{c['text']}")
    return "\n\n".join(parts)
